<p>Указывается максимальное количество подземных этажей.</p>
<p>
    Пример:<br>
    здание 1 - нет подземных этажей;<br>
    здание 2 - один подземный этаж;<br>
    здание 3 - два подземных этажа.<br>
    Следовательно, в поле "Количество подземных этажей" указывается - 2 (два подземных этажа).
</p>
